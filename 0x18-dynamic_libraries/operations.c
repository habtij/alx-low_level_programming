#include "main.h"
#include <stdio.h>

/**
 * add - summation of a and b
 * @a: first operand
 * @b: second operand
 *
 * Return: the sum of a and b
 */
int add(int a, int b)
{
  return (a + b);
}

/**
 * sub - subtract a from b
 * @a: first operand
 * @b: second operand
 *
 * Return: subtraction of a from b
 */
int sub(int a, int b)
{
  return (a - b);
}

/**
 * mul - multiply a and b
 * @a: first operand
 * @b: second operand
 *
 * Return: multiplication of a and b
 */
int mul(int a, int b)
{
  return (a * b);
}

/**
 * div - divides a from b
 * @a: first operand
 * @b: second operand
 *
 * Return: division of a from b
 */
int div(int a, int b)
{
  return (a / b);
}

/**
 * mod - remainder from dividing a from b
 * @a: first operand
 * @b: second operand
 *
 * Return: remainder from dividing a from b
 */
int mod(int a, int b)
{
  return (a % b);
}
